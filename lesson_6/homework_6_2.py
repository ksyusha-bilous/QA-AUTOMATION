"""Цикл “Дочекайся літери”
Напишіть цикл, який буде вимагати від користувача ввести слово, в якому є літера "h" 
(враховуються як великі так і маленькі). 
Цикл не повинен завершитися, якщо користувач ввів слово без букви "h"."""


while True:
    word = input("Введіть слово з літерою 'h': ")
    if 'h' in word.lower():  # перевіряємо, чи є 'h' незалежно від регістру
        print("Введене слово містить 'h'. Дякуємо!")
        break
    else:
        print("Слова без 'h' не підходять. Спробуйте ще раз.")



"""
word.lower() → перетворює слово на маленькі літери, щоб враховувати і великі 'H'.
if 'h' in word.lower(): → перевіряє наявність букви 'h'.
break → вихід з циклу, коли умова виконана."""